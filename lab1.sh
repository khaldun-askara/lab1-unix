echo "grep"
echo "Файл с именами:"
cat names.txt
echo ""
echo "1 Иннокентии и Харитоны:"
grep -E 'Иннокентий|Харитон' names.txt
echo ""
echo "2 Фамилии на согласные:"
grep -P '(?=[^АЕЁИОУЫЭЮЯ])[А-Я][а-яё]* [А-ЯЁа-яё]* [А-ЯЁа-яё]*' names.txt
echo ""
echo "3 Минимум 3 гласных в начале фамилии:"
grep -cE '[АЕЁИОУЫЭЮЯаеёиоуыэюя]{3,}[а-яё]* [А-ЯЁа-яё]* [А-ЯЁа-яё]*' names.txt
echo ""
echo "4 Последняя фамилия по алфавиту:"
cat names.txt | sort | tail -1
